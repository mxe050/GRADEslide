// Bullet を mobile-friendly に整形 (長すぎる文を分割しない、ただし末尾の ＊メモを別 bullet に)
function trimBullets(bullets) {
  return bullets
    .map((b) => b.trim())
    .filter(Boolean)
    .map((b) => b.replace(/\s+/g, " "));
}

